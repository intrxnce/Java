package aula02;

public class operadores {

	public static void main(String[] args) {
		
		int n1 =2 , n2 =2;
		
		System.out.println("variável n1: " + n1);
		System.out.println("variável n1: " + (++n1));

		System.out.println("variável n2: " + n2);
		System.out.println("variável n2: " + (n2++));
		System.out.println("variável n2: " + (n2 += 2));
		
	}
}
